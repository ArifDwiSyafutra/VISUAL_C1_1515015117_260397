    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        
        String nama_edit = jTable1.getValueAt(baris, 0).toString();
        String notelp_edit = jTable1.getValueAt(baris, 1).toString();
        String kotaasal_edit = jTable1.getValueAt(baris, 2).toString();
        
        Tnama.setText(nama_edit);
        Ttelp.setText(notelp_edit);
        Ckota.setSelectedItem(kotaasal_edit);
    }//GEN-LAST:event_jTable1MouseClicked

