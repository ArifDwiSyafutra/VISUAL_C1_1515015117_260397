    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(Tnama.getText().length()==0 || Ttelp.getText().length()==0){
            JOptionPane.showMessageDialog(null,"NIM dan NAMA KOSONG");
        }
        else{
        int baris = jTable1.getSelectedRow();
        
        jTable1.setValueAt(Tnama.getText(), baris, 0);
        jTable1.setValueAt(Ttelp.getText(), baris, 1);
        jTable1.setValueAt(Ckota.getSelectedItem(), baris, 2);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void BsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsimpanActionPerformed
        // TODO add your handling code here:
        if(Tnama.getText().length()==0 || Ttelp.getText().length()==0){
            JOptionPane.showMessageDialog(null,"Keterangan tidak boleh kosong !!!");
        }
         else{
        Object[] data = new Object[3];
        data[0]=Tnama.getText();
        data[1]=Ttelp.getText();
        data[2]=Ckota.getSelectedItem().toString();
       
       
        model.addRow(data);
         }
    }//GEN-LAST:event_BsimpanActionPerformed

