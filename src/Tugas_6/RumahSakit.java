    private void BsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BsimpanActionPerformed
        // TODO add your handling code here:
        int simpan=JOptionPane.showConfirmDialog(this, "Apakah anda ingin menyimpan data","Confirm Simpan",
        JOptionPane.YES_OPTION);
        if(simpan==JOptionPane.YES_OPTION){
        String nama_pasien = txtNama.getText();
        String tl1 = tl.getText();
        String usia = txtUsia.getText();
        String jk;
        if(laki.isSelected()){
            jk = laki.getText();
        }
        else if(pr.isSelected()){
            jk =pr.getText();
        }
        else{
            JOptionPane.showMessageDialog(null,"Pilih Jenis Kelamin",
                    "Warning",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String Dokter = cmbDokter.getSelectedItem().toString();
        String Ruangan = cmbRuangan.getSelectedItem().toString();
        String Tgl_masuk= tm.getText();
        String diagnosa = listDiagnosa.getSelectedValuesList().toString();
        TambahData(nama_pasien, tl1, usia, jk,diagnosa, Dokter, Ruangan,Tgl_masuk);
    }
    }//GEN-LAST:event_BsimpanActionPerformed

